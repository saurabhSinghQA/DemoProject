package com.dataTypes;

public class DataTypes {
	
	 static float a; /// declaring 

	public static void main(String[] args) {
		//int data type
	   int i= 10;  //initialization
	   System.out.println(i);
	   
	   //boolean data type
	   boolean flag= true;
	   System.out.println(flag);
	   
	   //double data type
	   
	   double d= 12.3333333333333333333333333333333333333333333333333333333333333333333333333;
	   System.out.println(d);
	   
	   //float data type
	   
	   float f= 2.5f;
	   System.out.println(f);
	   
	   //non primitive String data type
	   String s= "megha";
	   System.out.println(s);
	   
	   short sh= 24;
	   System.out.println(sh);
	   
	   //character data type
	   
	   char c ='a';
	   System.out.println(c);
	   
	   
	   
	   System.out.println(a);
	   
	   
		
		

	}

}
